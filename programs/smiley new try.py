import turtle as t
import time as s
c=["Red","Blue","orange"]
t.pensize(4)
t.color(c[2])
t.penup()
t.sety(-150)
t.pendown()
t.circle(200)
t.penup()
t.sety(100)
t.pendown()
t.penup()
t.forward(80)
t.pendown()
t.color(c[0])
t.circle(7)
t.circle(13)
t.color(c[1])
t.circle(25)
t.penup()
t.backward(160)
t.pendown()
t.color(c[0])
t.circle(7)
t.circle(13)
t.color(c[1])
t.circle(25)
t.penup()
t.forward(80)
t.pendown()
t.penup()
t.sety(-100)
t.pendown()
t.color("Orange")
for i in range(360):
    t.left(1)
    t.forward(2)
    if(i==80):
        t.penup()
    if(i==280):
        t.pendown()
s.sleep(10)
